/*
PrivateKey :-
    Private keys can be used for both encryption and decryption.
    Private keys are shared between the sender and the receiver.
         
PublicKey :-    
    Public keys are used only for the purpose of encrypting the sensitive data.  
    Public keys can be freely circulated among multiple users.
*/

/*
 - User Sign the Message using his own private key.
 - Miner verify the transaction using verification function which takes user's Public key,
   Message and signature hash.
*/

